#include <stdio.h>
#include <stdlib.h>
#include <string.h>

#include <stdint.h>
#include "cli.h"

/******************************************************************************
 *                           Defines and typedefs
 ******************************************************************************/
#define REC_INPUT_SIZE_IN_BYTES     10

/******************************************************************************
 *                           external variables
 ******************************************************************************/

/******************************************************************************
 *                           Local variables
 ******************************************************************************/
static char pcRxBuffer[REC_INPUT_SIZE_IN_BYTES];

/******************************************************************************
 *                           Local Functions
 ******************************************************************************/

/******************************************************************************
 *                    G L O B A L  F U N C T I O N S
 ******************************************************************************/
/** @brief main function
 *  @param[in]  None
 *  @return     int
 */
int main(void)
{
	system ("/bin/stty raw");
	CliInit();

	for(;;)
	{
		uint8_t ucBytesReceived = 0;

		pcRxBuffer[0] = getchar();
		ucBytesReceived = 1;

		if (0!= ucBytesReceived)
		{
			CliProcessCommand(pcRxBuffer, ucBytesReceived);
		}

	}

	return 0;
}//end main

